#include "3-calc.h"

/**
 * op_add - Returns the sum of two numbers.
 * @a: the first number
 * @b: the second number
 *
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns the differnece of two numbers.
 * @a: the first num
 * @b: the second num
 *
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the prduct of to nums
 * @a: first num
 * @b: second nm
 *
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division of two nums
 * @a: first num
 * @b: second num
 *
 * Return: the suotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of te division of a and b
 * @a: first num
 * @b: second num
 *
 * Return: the remainder of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
